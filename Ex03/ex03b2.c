#include<stdio.h>

int main(){
  int a,b,c,d,e,ao,bo,co,doo,eo,sum;
  double av;
  printf("国語の点数と重みを入力してください : ");
  scanf("%d %d", &a, &ao);
  printf("数学の点数と重みを入力してください : ");
  scanf("%d %d", &b, &bo);
  printf("英語の点数と重みを入力してください : ");
  scanf("%d %d", &c, &co);
  printf("理科の点数と重みを入力してください : ");
  scanf("%d %d", &d, &doo);
  printf("社会の点数と重みを入力してください : ");
  scanf("%d %d", &e, &eo);
  sum = a*ao + b*bo + c*co + d*doo + e*eo;
  av = (double) sum / (ao+bo+co+doo+eo);
  printf("５教科の重み付き合計点は%d点で、重み付き平均点は%f点です。\n",sum,av);
  return 0;
}
